package com.example.smartnotepad2;

public class Pic
{
    private String mName;
    private int mId;

    public Pic(String name, int Id){
        mName = name;
        mId = Id;
    }

    public String getmName() {
        return mName;
    }

    public int getmId() {
        return mId;
    }
}
